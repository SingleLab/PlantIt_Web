// Language manager
const LanguageManager = {
    currentLang: 'en',
    
    init() {
        // Load saved language preference or default to English
        this.currentLang = localStorage.getItem('plantit-lang') || 'en';
        this.applyTranslations();
        this.updateToggleButton();
    },
    
    toggleLanguage() {
        this.currentLang = this.currentLang === 'en' ? 'ca' : 'en';
        localStorage.setItem('plantit-lang', this.currentLang);
        this.applyTranslations();
        this.updateToggleButton();
    },
    
    applyTranslations() {
        const lang = translations[this.currentLang];
        
        // Update all elements with data-i18n attribute
        document.querySelectorAll('[data-i18n]').forEach(element => {
            const key = element.getAttribute('data-i18n');
            if (lang[key]) {
                if (element.tagName === 'META') {
                    element.setAttribute('content', lang[key]);
                } else if (element.hasAttribute('data-i18n-html')) {
                    element.innerHTML = lang[key];
                } else {
                    element.textContent = lang[key];
                }
            }
        });
        
        // Update page title
        const titleElement = document.querySelector('title');
        if (titleElement && titleElement.hasAttribute('data-i18n')) {
            const titleKey = titleElement.getAttribute('data-i18n');
            if (lang[titleKey]) {
                titleElement.textContent = lang[titleKey];
            }
        }
        
        // Update HTML lang attribute
        document.documentElement.lang = this.currentLang;
    },
    
    updateToggleButton() {
        const toggleBtn = document.getElementById('lang-toggle');
        if (toggleBtn) {
            toggleBtn.textContent = translations[this.currentLang]['lang-toggle'];
        }
    }
};

// Initialize when DOM is ready
document.addEventListener('DOMContentLoaded', () => {
    LanguageManager.init();
});
